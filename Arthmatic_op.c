#include <stdio.h>

int main() {
    int a = 10, b = 5;

    // Addition
    int sum = a + b;
    printf("Sum: %d\n", sum);

    // Subtraction
    int difference = a - b;
    printf("Difference: %d\n", difference);

    // Multiplication
    int product = a * b;
    printf("Product: %d\n", product);

    // Division
    int quotient = a / b;
    printf("Quotient: %d\n", quotient);

    // Modulus
    int remainder = a % b;
    printf("Remainder: %d\n", remainder);

    return 0;
}
